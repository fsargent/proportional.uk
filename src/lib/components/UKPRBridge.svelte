	<div class="bridge-copy">
		<h2 id="uk-pr-bridge-title" class="section-header">Start from the UK PR conversation we already have</h2>
		<p class="intro-text">
			UK reform politics does not begin from a blank sheet. People already know the main proportional
			options: <strong>STV</strong> for candidate-centred multi-member districts, <strong>AMS/MMP</strong>
			for a local-plus-top-up model, and <strong>List PR</strong> for straightforward party
			proportionality.
		</p>
		<p>
			This site is not dismissing that conversation. It builds from it. The claim is narrower and more
			practical: whichever proportional family you favour, the ballot and counting rules can often be
			made more legible by replacing forced ranking or single-choice local votes with approval-based
			ones.
		</p>
	</div>

	<div class="bridge-grid">
		<article class="bridge-card">
			<h3>STV</h3>
			<p>Strong local pluralism and no closed lists, but larger districts and ranked counting add complexity.</p>
		</article>
		<article class="bridge-card">
			<h3>AMS / MMP</h3>
			<p>Familiar in Scotland and Wales, preserving local seats while using top-ups to restore proportionality.</p>
		</article>
		<article class="bridge-card">
			<h3>List PR</h3>
			<p>Direct route to proportionality, especially with open lists, but it shifts more politics to party-level competition.</p>
		</article>
	</div>
</section>

